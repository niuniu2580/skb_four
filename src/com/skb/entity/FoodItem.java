package com.skb.entity;

import android.graphics.Bitmap;

public class FoodItem {
	
	private String itemTitle;
	private Bitmap firstItemResId;
	private String firstItemMsg;
	private Bitmap secondItemResId;
	private String secondItemMsg;
	private Bitmap thirdItemResId;
	private String thirdItemMsg;
	public String getItemTitle() {
		return itemTitle;
	}
	public void setItemTitle(String itemTitle) {
		this.itemTitle = itemTitle;
	}
	public Bitmap getFirstItemResId() {
		return firstItemResId;
	}
	public void setFirstItemResId(Bitmap firstItemResId) {
		this.firstItemResId = firstItemResId;
	}
	public String getFirstItemMsg() {
		return firstItemMsg;
	}
	public void setFirstItemMsg(String firstItemMsg) {
		this.firstItemMsg = firstItemMsg;
	}
	public Bitmap getSecondItemResId() {
		return secondItemResId;
	}
	public void setSecondItemResId(Bitmap secondItemResId) {
		this.secondItemResId = secondItemResId;
	}
	public String getSecondItemMsg() {
		return secondItemMsg;
	}
	public void setSecondItemMsg(String secondItemMsg) {
		this.secondItemMsg = secondItemMsg;
	}
	public Bitmap getThirdItemResId() {
		return thirdItemResId;
	}
	public void setThirdItemResId(Bitmap thirdItemResId) {
		this.thirdItemResId = thirdItemResId;
	}
	public String getThirdItemMsg() {
		return thirdItemMsg;
	}
	public void setThirdItemMsg(String thirdItemMsg) {
		this.thirdItemMsg = thirdItemMsg;
	}
	
	
}
